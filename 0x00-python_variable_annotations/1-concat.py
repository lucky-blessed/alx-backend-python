#!/usr/bin/python3
"""
Module to concatenate two strings
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenates two strings and return the result.

    Args:
        str1 (str): The first string.
        str2 (str): The second string.

    Returns:
        str: The concatenated stringy.
    """
    return str1 + str2
