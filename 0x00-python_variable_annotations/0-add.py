#!/usr/bin/env python3
"""
Module to add two floats a and b
"""


def add(a: float, b: float) -> float:
    """
    Adds two float numbers and return the result.

    Args:
        a (float): The first float number.
        b (float): The second float number.

    Returns:
        float: The sum of a and b.
    """
    return a + b
